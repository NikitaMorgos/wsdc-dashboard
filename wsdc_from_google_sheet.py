def ru_to_en(name_ru: str) -> str:
    """
    Транслитерация русского имени в английское (WSDC-стиль).
    
    Args:
        name_ru: Имя на русском (формат: "Фамилия Имя" или "Фамилия Имя Отчество")
    
    Returns:
        Имя на английском в формате "Имя Фамилия"
    
    Примеры:
        >>> ru_to_en("Кочев Алексей")
        'Alexey Kochev'
        >>> ru_to_en("Иванова Татьяна")
        'Tatiana Ivanova'
        >>> ru_to_en("Захаров Дмитрий")
        'Dmitry Zakharov'
    """
    if not name_ru:
        return ""
    
    name_ru = name_ru.strip()
    
    # Проверяем полное ФИО в исключениях
    if name_ru in FULL_NAME_EXCEPTIONS:
        return FULL_NAME_EXCEPTIONS[name_ru]
    
    # Разбиваем на части (фамилия, имя, отчество)
    parts = name_ru.split()
    result_parts = []
    
    for part in parts:
        # Проверяем, есть ли это слово в исключениях
        if part in NAME_EXCEPTIONS:
            result_parts.append(NAME_EXCEPTIONS[part])
        else:
            # Транслитерируем посимвольно
            transliterated = ""
            for char in part:
                if char in TRANSLIT_TABLE:
                    transliterated += TRANSLIT_TABLE[char]
                else:
                    transliterated += char  # Оставляем как есть (латиница, цифры и т.д.)
            result_parts.append(transliterated)
    
    # Формат в Google Sheet: "Фамилия Имя" или "Фамилия Имя Отчество"
    # Для WSDC нужен формат: "Имя Фамилия"
    
    if len(result_parts) == 3:
        # Формат: Фамилия Имя Отчество -> Имя Фамилия (отчество отбрасываем)
        return f"{result_parts[1]} {result_parts[0]}"
    elif len(result_parts) == 2:
        # Формат: Фамилия Имя -> Имя Фамилия
        return f"{result_parts[1]} {result_parts[0]}"
    else:
        return " ".join(result_parts)


