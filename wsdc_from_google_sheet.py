def search_wsdc_dancer(name_en: str, session: requests.Session) -> List[Dict[str, Any]]:
    """
    Поиск танцора в WSDC по имени.
    
    Args:
        name_en: Имя на английском
        session: requests Session
    
    Returns:
        Список найденных танцоров с их данными
    """
    params = {"q": name_en}
    
    try:
        response = session.get(WSDC_SEARCH_API, params=params, headers=HEADERS, timeout=30)
        
        # Не используем raise_for_status(), проверяем вручную
        if response.status_code == 404:
            # 404 может означать "не найдено" или rate limiting
            return []
        
        if response.status_code != 200:
            print(f"  HTTP {response.status_code} для '{name_en}'", flush=True)
            return []
        
        try:
            data = response.json()
        except Exception:
            return []
        
        if data.get("type") == "names" and "names" in data:
            # Убираем дубликаты по wsdc_id
            seen_ids = set()
            unique_names = []
            for dancer in data["names"]:
                if dancer["wsdc_id"] not in seen_ids:
                    seen_ids.add(dancer["wsdc_id"])
                    unique_names.append(dancer)
            return unique_names
        
        return []
    
    except requests.exceptions.Timeout:
        print(f"  Таймаут для '{name_en}'", flush=True)
        return []
    except requests.exceptions.ConnectionError:
        print(f"  Ошибка соединения для '{name_en}'", flush=True)
        return []
    except Exception as e:
        print(f"  Ошибка поиска '{name_en}': {e}", flush=True)
        return []


def get_dancer_points_from_html(wsdc_id: int, session: requests.Session) -> Dict[str, Any]:
    """
    Получает поинты танцора, парся HTML страницы.
    
    Примечание: WSDC использует JavaScript для рендеринга данных,
    поэтому этот метод может не работать для всех случаев.
    Используется requests_html как fallback.
    
    Args:
        wsdc_id: WSDC ID танцора
        session: requests Session
    
    Returns:
        Словарь с данными танцора
    """
    url = f"{WSDC_LOOKUP_URL}?q={wsdc_id}"
    
    try:
        response = session.get(url, headers=HEADERS, timeout=30)
        response.raise_for_status()
        
        soup = BeautifulSoup(response.text, 'html.parser')
        
        result = {
            "wsdc_id": wsdc_id,
            "name": None,
            "divisions": []
        }
        
        # CSS-селекторы для парсинга страницы WSDC
        # ВАЖНО: Если верстка изменится, нужно обновить эти селекторы
        
        # Селектор для имени танцора (h1 с именем и ID)
        # Пример: "Jordan Frisbee (311)"
        name_heading = soup.find('h1')
        if name_heading:
            name_text = name_heading.get_text(strip=True)
            # Извлекаем имя без ID в скобках
            match = re.match(r'^(.+?)\s*\(\d+\)$', name_text)
            if match:
                result["name"] = match.group(1).strip()
        
        # Селектор для секций с дивизионами
        # Ищем h2 с текстом типа "West Coast Swing - Leader"
        role_headings = soup.find_all('h2')
        
        for role_h2 in role_headings:
            role_text = role_h2.get_text(strip=True)
            # Пример: "West Coast Swing - Leader"
            if ' - ' in role_text:
                parts = role_text.split(' - ')
                if len(parts) >= 2:
                    role = parts[-1].strip()  # Leader / Follower
                    
                    # Ищем h3 с дивизионами после этого h2
                    # Пример: "Novice 24 points"
                    next_elem = role_h2.find_next_sibling()
                    while next_elem and next_elem.name != 'h2':
                        if next_elem.name == 'h3':
                            div_text = next_elem.get_text(strip=True)
                            # Парсим "Division XX points"
                            match = re.match(r'^(\w+(?:\s+\w+)?)\s+(\d+)\s*points?$', div_text, re.IGNORECASE)
                            if match:
                                division = match.group(1).strip()
                                points = int(match.group(2))
                                result["divisions"].append({
                                    "division": division,
                                    "role": role,
                                    "points": points
                                })
                        next_elem = next_elem.find_next_sibling()
        
        return result
    
    except Exception as e:
        print(f"  Ошибка получения данных для ID {wsdc_id}: {e}", flush=True)
        return {"wsdc_id": wsdc_id, "name": None, "divisions": []}


def get_dancer_points_via_api(wsdc_id: int, session: requests.Session) -> Dict[str, Any]:
    """
    Получает поинты танцора через API эндпоинт /lookup2020/find.
    
    Это более быстрый и надёжный способ, чем Playwright.
    
    Args:
        wsdc_id: WSDC ID танцора
        session: requests Session
    
    Returns:
        Словарь с данными танцора
    """
    result = {
        "wsdc_id": wsdc_id,
        "name": None,
        "divisions": []
    }
    
    try:
        # Сначала получаем CSRF токен из формы
        response = session.get(WSDC_LOOKUP_URL, headers=HEADERS, timeout=30)
        response.raise_for_status()
        
        soup = BeautifulSoup(response.text, 'html.parser')
        token_input = soup.find('input', {'name': '_token'})
        if not token_input:
            print(f"    Не найден CSRF токен", flush=True)
            return result
        
        token = token_input.get('value', '')
        if not token:
            print(f"    CSRF токен пустой", flush=True)
            return result
        
        # Делаем POST запрос к /lookup2020/find с wsdc_id
        find_url = "https://points.worldsdc.com/lookup2020/find"
        data = {
            '_token': token,
            'num': wsdc_id
        }
        
        response = session.post(find_url, data=data, headers=HEADERS, timeout=30)
        response.raise_for_status()
        
        # Ответ должен быть JSON или HTML
        try:
            json_data = response.json()
        except:
            # Если не JSON, парсим HTML
            soup = BeautifulSoup(response.text, 'html.parser')
            
            # Ищем имя в h1
            h1 = soup.find('h1')
            if h1:
                name_text = h1.get_text(strip=True)
                match = re.match(r'^(.+?)\s*\(\d+\)$', name_text)
                if match:
                    result["name"] = match.group(1).strip()
            
            # Ищем дивизионы и роли
            h2_elements = soup.find_all('h2')
            for h2 in h2_elements:
                h2_text = h2.get_text(strip=True)
                if ' - ' in h2_text and ('Leader' in h2_text or 'Follower' in h2_text):
                    parts = h2_text.split(' - ')
                    if len(parts) >= 2:
                        role = parts[-1].strip()
                        
                        # Ищем h3 после этого h2
                        next_elem = h2.find_next_sibling()
                        while next_elem and next_elem.name != 'h2':
                            if next_elem.name == 'h3':
                                div_text = next_elem.get_text(strip=True)
                                match = re.match(r'^([\w-]+(?:\s+[\w-]+)?)\s+(\d+)\s*points?$', div_text, re.IGNORECASE)
                                if match:
                                    division = match.group(1).strip()
                                    points = int(match.group(2))
                                    result["divisions"].append({
                                        "division": division,
                                        "role": role,
                                        "points": points
                                    })
                            next_elem = next_elem.find_next_sibling()
            
            return result
        
        # Обрабатываем JSON ответ
        # Структура: {"leader": {...}, "follower": {...}}
        # Каждая роль содержит: dancer, level, placements
        
        dancer_info = None
        roles_data = []
        
        # Проверяем leader
        if 'leader' in json_data:
            leader_data = json_data['leader']
            if leader_data.get('type') == 'dancer':
                roles_data.append(('Leader', leader_data))
                if not dancer_info:
                    dancer_info = leader_data.get('dancer', {})
        
        # Проверяем follower
        if 'follower' in json_data:
            follower_data = json_data['follower']
            if follower_data.get('type') == 'dancer':
                roles_data.append(('Follower', follower_data))
                if not dancer_info:
                    dancer_info = follower_data.get('dancer', {})
        
        # Извлекаем имя
        if dancer_info:
            first_name = dancer_info.get('first_name', '')
            last_name = dancer_info.get('last_name', '')
            result["name"] = f"{first_name} {last_name}".strip()
        
        # Извлекаем поинты для каждой роли
        for role_name, role_data in roles_data:
            placements = role_data.get('placements', {})
            
            # placements структура: {"West Coast Swing": {"NOV": {...}, "INT": {...}, ...}}
            if isinstance(placements, dict):
                for dance_type, divisions in placements.items():
                    if isinstance(divisions, dict):
                        for div_abbr, div_data in divisions.items():
                            if isinstance(div_data, dict) and 'total_points' in div_data:
                                division_name = div_data.get('division', {}).get('name', div_abbr)
                                total_points = div_data.get('total_points', 0)
                                
                                if total_points > 0:
                                    result["divisions"].append({
                                        "division": division_name,
                                        "role": role_name,
                                        "points": int(total_points)
                                    })
        
        # Альтернативная структура: прямой тип "dancer"
        if json_data.get('type') == 'dancer':
            dancer = json_data.get('dancer', {})
            if not result["name"]:
                result["name"] = dancer.get('name') or f"{dancer.get('first_name', '')} {dancer.get('last_name', '')}".strip()
        
        # Альтернативная структура: тип "names"
        elif json_data.get('type') == 'names':
            names = json_data.get('names', [])
            if names and not result["name"]:
                first = names[0]
                result["name"] = first.get('dancer_name', '')
    
    except requests.exceptions.RequestException as e:
        print(f"    Ошибка API запроса: {e}", flush=True)
    except Exception as e:
        print(f"    Ошибка обработки данных: {e}", flush=True)
    
    return result


